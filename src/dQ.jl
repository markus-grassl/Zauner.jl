    dq(k)

Precomputed values of all admissible tuples `(d,Q)`
with `d ≤ 256`, comprising the the first 3292 such tuples
when ordered by dimension.
The forms `Q` have been chosen (non-uniquely) to minimize
the period of the Hirzebruch-Jung continued fraction expansion of their positive root.

# Examples

```jldoctest
julia> dq(1)
(4, Binary quadratic form over ZZ: x^2 - 3*x*y + y^2)
```
